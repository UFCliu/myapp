$('.drag').draggable({ 
  appendTo: 'body',
  helper: 'clone'
});

$('#dropzone').droppable({
  activeClass: 'active',
  hoverClass: 'hover',
  accept: ":not(.ui-sortable-helper)", // Reject clones generated by sortable
  drop: function (e, ui) {
	cleanDefult();
	var dragHtml = ui.draggable.html();
	
	dragHtml += "<input type='file' name='file' onclick='addTempImg(this)' style='display:none;' class='addImg' >" 
			 + "<button type='button' class='myButton' onclick='addImgObj(this)'>"
			 +	 "<span class='glyphicon glyphicon-picture'></span>"
			 +	 "</button>"
			 +	 "<button class='myButton' type='button' onclick='deleteObj(this)'>"
			 +	 "<span class='glyphicon glyphicon-trash'></span>"
			 +	 "</button>"
    var $el = $('<div class="drop-item"><details><summary onclick="showImg(this)">' + dragHtml + '</summary></details></div>');
   // $el.append($('<button type="button" class="btn btn-default btn-xs remove" style="right:30px"><span class="glyphicon glyphicon-picture"></span></button>').click(function () { var detailsDiv = $(this).parent().children("details").children("summary");$(detailsDiv).find(".addImg").trigger("click"); }));
    //$el.append($('<button type="button" class="btn btn-default btn-xs remove"><span class="glyphicon glyphicon-trash"></span></button>').click(function () { $(this).parent().detach(); }));
    $(this).append($el);
  }
}).sortable({
  items: '.drop-item',
  sort: function() {
    // gets added unintentionally by droppable interacting with sortable
    // using connectWithSortable fixes this, but doesn't allow you to customize active/hoverClass options
    $( this ).removeClass( "active" );
  }
});